export function buildRichContent(route) {
  // 주유카드 카드사별
  const issuerMatch = route.match(/^\/fuel-card\/(hyundai|shinhan|kb|samsung|lotte|hana)$/);
  if (issuerMatch) {
    return injectCommonSection(buildFuelCardIssuerContent(issuerMatch[1]));
  }

  // 주유카드 유종별
  const fuelTypeMatch = route.match(/^\/fuel-card\/(gasoline|diesel|lpg)$/);
  if (fuelTypeMatch) {
    return injectCommonSection(buildFuelTypeContent(fuelTypeMatch[1]));
  }

  // 주유카드 월 주유비별
  const monthlyMatch = route.match(/^\/fuel-card\/monthly\/(\d+)$/);
  if (monthlyMatch) {
    const amount = Number.parseInt(monthlyMatch[1], 10);
    if (Number.isFinite(amount) && amount > 0) {
      return injectCommonSection(buildFuelMonthlyContent(amount));
    }
  }

  // 해외결제 통화별
  const currencyMatch = route.match(/^\/overseas-payment\/(usd|eur|jpy|gbp|cny|thb|vnd)$/);
  if (currencyMatch) {
    return injectCommonSection(buildOverseasCurrencyContent(currencyMatch[1]));
  }

  // 정적 카테고리 (이미 buildStaticContent 내에서 공통 섹션 포함)
  const staticContent = buildStaticContent(route);
  if (staticContent) return staticContent;

  return null;
}

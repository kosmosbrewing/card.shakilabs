function buildStaticContent(route) {
  const data = STATIC_CATEGORIES[route];
  if (!data) return null;

  const sectionsHtml = data.sections
    .map((s) => `<h2 style="${H2}">${s.h2}</h2><p style="${P}">${s.body}</p>`)
    .join("");

  const linksHtml = data.links
    .map((l) => `<li style="${LI}"><a href="${l.path}">${l.label}</a></li>`)
    .join("");

  // privacy/terms는 법적 페이지라 공통 섹션 미포함
  const isLegal = route === "/privacy" || route === "/terms";
  const extraSection = isLegal ? "" : buildCommonExtraSection();

  return `
    <article data-seo-prerender="static" style="${ARTICLE}">
      <h1 style="${H1}">${data.title}</h1>
      <p style="${P}">${data.intro}</p>
      ${sectionsHtml}
      ${extraSection}
      <h2 style="${H2}">관련 페이지</h2>
      <ul style="${UL}">${linksHtml}</ul>
      <p style="font-size:12px;color:#64748b;margin-top:24px;">
        ※ 본 페이지는 공개된 카드사 정보를 기반으로 작성되었으며, 최종 가입 조건은 해당 카드사 공식 페이지에서 확인하시기 바랍니다.
      </p>
    </article>`;
}

// 동적 빌더에도 공통 섹션 주입: </article> 바로 전에 삽입
function injectCommonSection(html) {
  if (!html) return html;
  const common = buildCommonExtraSection();
  // </article> 태그 앞에 삽입
  return html.replace(/<\/article>/, `${common}</article>`);
}

